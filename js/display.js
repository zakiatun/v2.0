function beranda() {
    var a = document.getElementById("beranda");
    var b = document.getElementById("semuaDoa");
    var c = document.getElementById("doaFavorit");
    var x = document.getElementById("pengulangan");
    var y = document.getElementById("tentang");
    var z = document.getElementById("bantuan");
    var i = document.getElementById("infoDoa");
    if (a.style.display === "none") {
        a.style.display = "block";
        b.style.display = "none";
        c.style.display = "none";
        x.style.display = "none";
        y.style.display = "none";
        z.style.display = "none";
        i.style.display = "none";
    } else {
        a.style.display = "none";
        b.style.display = "none";
        c.style.display = "none";
        x.style.display = "none";
        y.style.display = "none";
        z.style.display = "none";
        i.style.display = "block";
    }
  }

